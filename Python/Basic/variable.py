# TRING
name = 'Nhi'
first_name = "Nguyen"
full_name = first_name + " " + name
print(type(name))
print('Hello ' + name)
print('My name is ' + full_name)

# INTEGER
age = 21
age = 21 - 1
print(age)
print(type(age))
# print("My age is " + age) // errors
print('My age: ' + str(age))

# FLOAT
height = 250.5
print(height)
print(type(height))
print('Your heigt is ' + str(height) + " cm")

# BOOLEAN
human = False
print(human)
print(type(human))
print('Are you a human: ' + str(human))